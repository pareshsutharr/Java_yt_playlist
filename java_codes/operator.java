public class operator {
    public static void main(String[] args) {
        int a=10;
        int b=40;
        String c = "hello paresh";
        System.out.println(a+" "+b+" "+c);
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a!=b);
        System.out.println(a==5 & b==04);
        System.out.println(a==5 | b==04);
    }
}
